void Conpairu() {
    ll b, c;
    cin >> b >> c;
    ll ans = 0;
    ll l1 = b - c / 2, r1 = b + max(c - 2, 0ll) / 2; // if c is 0. not -1
    ll l2 = -b - (c - 1) / 2, r2 = -b + (c - 1) / 2;
    ans += r1 - l1 + 1;
    ans += r2 - l2 + 1;
    ans -= max(0ll, min(r1, r2) - max(l1, l2) + 1);
    cout << ans << endl;
}
